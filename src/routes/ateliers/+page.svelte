<section>
    <header>
        <h2>Déroulement de l’atelier</h2>
    </header>
    <div>
        <p>
            Lors des premières séances, les participants découvriront les bases
            de l’écriture narrative et plongeront dans l’univers du conte grâce
            à des jeux interactifs. Une fois ces fondamentaux assimilés, ils
            écriront collectivement un conte, explorant ensemble les différentes
            facettes de sa construction. Enfin, un accompagnement pas à pas leur
            permettra de donner vie à leur propre conte.
        </p>
        <p>
            Un atelier est composé de cinq séances d’une heure et demi. Le
            nombre de séances et leur durée sont adaptables en fonction de vos
            besoins.
        </p>
    </div>
</section>
<section>
    <header><h2>Déroulement d’une séance</h2></header>
    <div>
        <p>
            La première partie de la séance consiste en un jeu d’écriture
            ludique autour du conte, suivi d’une proposition d’écriture où
            chacun peut explorer un aspect de la narration plus en profondeur,
            seul ou en groupe.<br />
            Durant la seconde partie de la séance, les participants qui le souhaitent
            pourront partager leurs écrits à voix haute, dans un esprit de convivialité.
        </p>
        <p>
            Une attention particulière est portée à créer un environnement
            chaleureux, où la créativité et le plaisir prennent toute leur
            place.
        </p>
    </div>
</section>
