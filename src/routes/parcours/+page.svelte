<section>
    <header>
        <h2>Auprès des autres</h2>
    </header>
    <div>
        <p>
            Avant que le conte ne croise ma route, mon chemin m’a mené au
            contact d’expériences humaines fortes : auprès des aînés, en donnant
            des cours d’histoire dans les maisons de retraite ; auprès des
            enfants, comme professeur des écoles en maternelle et en élémentaire
            ; et auprès des patients, à la médiathèque de l’hôpital Necker. Dans
            toutes ces expériences professionnelles, ce qui me guidait, c’était
            la rencontre et le partage. Ces aventures humaines m’ont appris
            combien il est essentiel de m’adapter aux besoins de chacun.
        </p>
    </div>
</section>
<section>
    <header>
        <h2>La rencontre</h2>
    </header>
    <div>
        <p>
            Puis, un jour, le conte est venu frapper à ma porte. J’ai été
            émerveillé par sa force tranquille, sa douceur et par la multitude
            de réponses qu’il semblait offrir à ma vie. J’ai eu envie de
            transmettre ces histoires, de créer des moments de partage et de
            faire ressentir la joie qu’elles apportent. Je me suis formé auprès
            de conteurs et conteuses, et j’ai pris la parole en scène ouverte,
            dans les écoles, les maisons de retraite, les bars et les hôpitaux.
        </p>
    </div>
</section>
<section>
    <header>
        <h2>L’écriture</h2>
    </header>
    <div>
        <p>
            Parallèlement, j’ai souhaité partager tout le plaisir et le
            bien-être que l’écriture de contes suscite en moi. En passant par
            l’imaginaire, les contes nous permettent d’utiliser des symboliques
            puissantes pour parler de nous et de la vie avec une perspective
            nouvelle, à la fois ludique et révélatrice. Afin de pouvoir
            transmettre au mieux cet art, j’ai suivi des formations à
            l’animation d’ateliers d’écriture, auprès de l’Esprit-Livre et de la
            Haute Ecole Léonard de Vinci à Bruxelles. Ces formations mettaient
            un accent particulier sur la mise en place d’ateliers d’écriture
            inclusifs, afin d’accompagner chacun dans l’exploration de sa
            créativité.
        </p>
    </div>
</section>
<section>
    <header>
        <h2>L’évidence</h2>
    </header>
    <div>
        <p>
            Après avoir partagé des instants précieux auprès des enfants, des
            personnes âgées et des patients, et après m’être enrichi des savoirs
            de conteurs et conteuses, j’ai choisi de consacrer ma vie à raconter
            des histoires. Aujourd’hui, je parcours différents lieux pour conter
            et animer des ateliers : auprès des tout-petits, des retraités, des
            personnes hospitalisées… Je m’adapte à chacun, convaincu que
            l’imaginaire est un trésor que nous portons tous en nous.
        </p>
    </div>
</section>
